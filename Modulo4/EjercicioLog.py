# Definir el número a evaluar
numero = 25

# Definir el rango
limite_inferior = 10
limite_superior = 50

# Verificar si está dentro del rango
dentro_del_rango = limite_inferior <= numero <= limite_superior

# Mostrar resultado
print(f"Número: {numero}")
print(f"Rango: [{limite_inferior} a {limite_superior}]")
print(f"¿Está dentro del rango? {dentro_del_rango}")
