# Definir las dimensiones
largo = 15
ancho = 7

# Calcular área usando multiplicación
area = largo * ancho

# Calcular perímetro usando suma y multiplicación
perimetro = 2 * (largo + ancho)

# Mostrar resultados
print(f"Dimensiones: {largo} x {ancho}")
print(f"Área: {area} unidades cuadradas")
print(f"Perímetro: {perimetro} unidades")
