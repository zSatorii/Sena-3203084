"""Módulo con funciones matemáticas básicas."""
PI = 3.14159

def cuadrado(x):
    """Calcula el cuadrado de un número."""
    return x ** 2

def cubo(x):
    """Calcula el cubo de un número."""
    return x ** 3

def area_circulo(radio):
    """Calcula el área de un círculo."""
    return PI * radio ** 2
